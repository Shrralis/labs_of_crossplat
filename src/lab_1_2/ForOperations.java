public class ForOperations {

    public static void main(String[] args) {
        int[]  x = {1, 2, 3, 5, 7, 9, 12, 34, 56, 65, 153};
        for (int i = 0; i < x.length; i++) {
            System.out.println("x[" + i + "]=" + x[i]);
        }
    }
}
