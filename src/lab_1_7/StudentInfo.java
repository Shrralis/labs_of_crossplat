public class StudentInfo {

    private float minimal; // мінімальний бал
    private float average;  // середній бал
    private float maximal; // маскимальний бал

    public float getMinimal() {
        return minimal;
    }

    public void setMinimal(float minimal) {
        this.minimal = minimal;
    }

    public float getAverage() {
        return average;
    }

    public void setAverage(float average) {
        this.average = average;
    }

    public float getMaximal() {
        return maximal;
    }

    public void setMaximal(float maximal) {
        this.maximal = maximal;
    }
}
